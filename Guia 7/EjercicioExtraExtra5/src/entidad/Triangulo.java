
package entidad;


public class Triangulo {
    private int lado;
    private int base;

    public Triangulo() {
    }

    public Triangulo(int lado, int base) {
        this.lado = lado;
        this.base = base;
    }

    public int getLado() {
        return lado;
    }

    public void setLado(int lado) {
        this.lado = lado;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }
    
    
}
